package model.DTO;

public class RoomDTO {
	String rmNo;
	String rmName;
	String rmChar;
	String rmFile;
	String rmContent;
	String rmPaycontent;

	public String getRmNo() {
		return rmNo;
	}

	public void setRmNo(String rmNo) {
		this.rmNo = rmNo;
	}

	public String getRmName() {
		return rmName;
	}

	public void setRmName(String rmName) {
		this.rmName = rmName;
	}

	public String getRmChar() {
		return rmChar;
	}

	public void setRmChar(String rmChar) {
		this.rmChar = rmChar;
	}

	public String getRmFile() {
		return rmFile;
	}

	public void setRmFile(String rmFile) {
		this.rmFile = rmFile;
	}

	public String getRmContent() {
		return rmContent;
	}

	public void setRmContent(String rmContent) {
		this.rmContent = rmContent;
	}

	public String getRmPaycontent() {
		return rmPaycontent;
	}

	public void setRmPaycontent(String rmPaycontent) {
		this.rmPaycontent = rmPaycontent;
	}
}