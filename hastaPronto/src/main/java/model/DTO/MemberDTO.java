package model.DTO;

import java.sql.Timestamp;
import java.util.Date;

public class MemberDTO {
	String userId;
	String userPassword;
	String userName;
	String userPh;
	String userEmail;
	String userAddr;
	Date userBirth;
	String userGender;
	Timestamp userRegist;
	StartEndPageDTO startEndPageDTO;
	
	
	public StartEndPageDTO getStartEndPageDTO() {
		return startEndPageDTO;
	}
	public void setStartEndPageDTO(StartEndPageDTO startEndPageDTO) {
		this.startEndPageDTO = startEndPageDTO;
	}
	public MemberDTO() {
	}
	public MemberDTO(String userId, String userPassword, String userName, String userPh, String userEmail,
			String userAddr, Date userBirth, String userGender, Timestamp userRegist) {
		this.userId = userId;
		this.userPassword = userPassword;
		this.userName = userName;
		this.userPh = userPh;
		this.userEmail = userEmail;
		this.userAddr = userAddr;
		this.userBirth = userBirth;
		this.userGender = userGender;
		this.userRegist = userRegist;
	}
	public Timestamp getUserRegist() {
		return userRegist;
	}
	public void setUserRegist(Timestamp userRegist) {
		this.userRegist = userRegist;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPh() {
		return userPh;
	}
	public void setUserPh(String userPh) {
		this.userPh = userPh;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserAddr() {
		return userAddr;
	}
	public void setUserAddr(String userAddr) {
		this.userAddr = userAddr;
	}
	public Date getUserBirth() {
		return userBirth;
	}
	public void setUserBirth(Date userBirth) {
		this.userBirth = userBirth;
	}
	public String getUserGender() {
		return userGender;
	}
	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}
}
